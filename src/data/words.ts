/** Get all categories */
export function getCategories(): string[] {
  return Object.keys(WORD_BANK);
}

/**
 * Get a random word from a specific category
 * @param category - Category name
 * @returns Random word from the category
 */
export function getRandomWordFromCategory(category: string): string {
  const words = WORD_BANK[category];
  if (!words || words.length === 0) {
    throw new Error(`Category "${category}" not found`);
  }
  return words[Math.floor(Math.random() * words.length)];
}

/**
 * Get a random word entry (word + category)
 * @returns Random word entry
 */
export function getRandomWord(): WordEntry {
  const categories = getCategories();
  const category = categories[Math.floor(Math.random() * categories.length)];
  const word = getRandomWordFromCategory(category);
  return { word, category };
}

/**
 * Get total word count
 * @returns Total number of words in the bank
 */
export function getTotalWordCount(): number {
  return Object.values(WORD_BANK).reduce((sum, words) => sum + words.length, 0);
}
